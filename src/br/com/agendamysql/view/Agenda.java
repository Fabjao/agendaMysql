    private void atualizaTabelaTelefone() {
        if (this.contato != null) {
            this.tmTelefone = new TableModelTelefone(this.contato.getTelefones());
        }
        tbTelefone.setModel(this.tmTelefone);
        tbTelefone.setAutoscrolls(true);

    }

    private void atualizaContato() {
        Long idContato = Long.parseLong(txtCodigo.getText());
        this.contato = this.contatoController.contatoPorId(idContato);
    }

    private void preencheTelaContato() {
        this.txtCodigo.setText(this.contato.getId().toString());
        this.txtNome.setText(this.contato.getNome());
        this.txtEmpresa.setText(this.contato.getEmpresa());
    }
