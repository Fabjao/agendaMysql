    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if (this.txtCodigo.getText().equals("")) {
            this.contato = new Contato();
            this.contato.setNome(txtNome.getText());
            this.contato.setEmpresa(txtEmpresa.getText());
            preencherDadosTelefone();
            this.contatoController.salva(this.contato);
            this.telefoneController.salvar(this.telefone);
            this.txtCodigo.setText(this.contato.getId().toString());
        } else {
            preencherDadosTelefone();
            this.telefoneController.salvar(this.telefone);
        }
        txtTelefone.setText("");
        atualizaTabelaTelefone();
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void preencherDadosTelefone() {
        this.telefone = new Telefone();
        this.telefone.setContato(this.contato);
        this.telefone.setOperador(cboOperadora.getSelectedItem().toString());
        this.telefone.setNumero(Integer.parseInt(txtTelefone.getText()));
        this.contato.getTelefones().add(telefone);
    }

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        novo();
    }//GEN-LAST:event_btnNovoActionPerformed

    private void novo() {
        this.txtCodigo.setText("");
        this.txtNome.setText("");
        this.txtEmpresa.setText("");
        this.txtTelefone.setText("");
        this.tbTelefone.setModel(new DefaultTableModel());
        this.contato = new Contato();
        this.telefone = new Telefone();
        this.telefones = new ArrayList();
        this.contatoController = new ContatoController();
    }

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        //this.contatoController.excluir(this.contato);
        if (tbTelefone.getSelectedRow() == -1) {
            this.contatoController.excluir(this.contato);
            novo();
        } else {
            Long idTelefone = Long.parseLong(tbTelefone.getValueAt(tbTelefone.getSelectedRow(), 0).toString());
            this.telefoneController.excluiPeloId(idTelefone);
            atualizaContato();
            atualizaTabelaTelefone();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ListaContato listaContato = new ListaContato(this, true);
        listaContato.preencherTabela();
        listaContato.setVisible(true);
        this.contato = listaContato.contato;
        if (this.contato != null) {
            atualizaTabelaTelefone();
            preencheTelaContato();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRelatorioActionPerformed
        List<Contato> contatos = new ArrayList<>();
        contatos.add(this.contato);
        this.contatoController.relatorio(contatos, chRelatorio.isSelected());
    }//GEN-LAST:event_btnRelatorioActionPerformed

